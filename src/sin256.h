inline int atan32(int xy32) {
  int sgn = xy32 < 0 ? -1 : 1;
  int i = abs(xy32);
  if (i > 127) {
    return sgn * 76;
  } else {
    return sgn * ATAN_32[i];
  }
}

inline int sin256 (int deg) {
	int pos = 1;
	if (deg < 0) {
		deg = -deg;
		pos = -1;
	}
	// deg >= 0
	deg %= 360;
	// deg <= 359
	if (deg >= 180) {
		deg -= 180;
		pos = -pos;
	}
	// deg <= 179
	if (deg >= 90)
		deg = 180 - deg;
	// deg <= 90
	return pos * SIN_LUT[deg];
}

